package palmos;

public final class FloatHolder {

  /** the 32-bit float value. */
  public float value;

  /** constructs a new FloatHolder instance with zero value. */
  public FloatHolder () {
    this.value = 0.0f;
  }

  /** constructs a new FloatHolder instance with the given value. */
  public FloatHolder (float value) {
    this.value = value;
  }
}
