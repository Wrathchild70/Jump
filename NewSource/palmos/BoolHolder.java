package palmos;

public final class BoolHolder {

  /** the boolean value. */
  public boolean value;

  /** constructs a new BoolHolder instance with value false. */
  public BoolHolder () {
    this.value = false;
  }

  /** constructs a new BoolHolder instance with the given value. */
  public BoolHolder (boolean value) {
    this.value = value;
  }
}
