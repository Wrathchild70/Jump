package palmos;

public final class IntHolder {

  /** the 32-bit int value. */
  public int value;

  /** constructs a new IntHolder instance with zero value. */
  public IntHolder () {
    this.value = 0;
  }

  /** constructs a new IntHolder instance with the given value. */
  public IntHolder (int value) {
    this.value = value;
  }
}
